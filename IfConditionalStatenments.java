package BasicJava;

public class IfConditionalStatenments {
    public static void main(String[] args) {
        int marks =80;
        if(marks >=90){
            System.out.println("You got A grade ");
        }else if(marks >=70){
            System.out.println("You got B grade");
        }else if(marks>=50){
            System.out.println("You passed");
        }else{
            System.out.println("You failed");
        }
    }
}
